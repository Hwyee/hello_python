""" 位运算符"""
A = 0B101
B = 0B011
print(A & B)  # 0B001
print(A | B)  # 0B111
print(A ^ B)  # 0B110
print(~A)  #  -6
print(~A + 1)  # -5 取反+1就是相反数
print(A << 2)  # 0B101 << 2 = 0B10100  左移两位
print(A >> 2)  # 0B101 >> 2 = 0B1 右移两位
print(A & 0B1111)  # 0B101 & 0B1111 = 0B101 和掩码进行与操作，还是本身
print(A | 0B1111)  # 0B101 | 0B1111 = 0B1111 和掩码进行或操作，结果是掩码

A = -0B101
print(A)
