""" 比较运算符 """
# 判断不相等
A = 10
B = 20
if A != B:
    print("A != B")
# 判断相等
if A == B:
    print("A == B")

# 判断大于
if A > B:
    print("A > B")
# 判断小于
if A < B:
    print("A < B")
# 判断大于等于
if A >= B:
    print("A >= B")
# 判断小于等于
if A <= B:
    print("A <= B")

# 字符串的比较 比较的是字符串的AScii码
S1 = "ABc"
S2 = "ABc"
if S1 == S2:
    print("S1 == S2")
if S1 != S2:
    print("S1 != S2")
if S1 > S2:
    print("S1 > S2")
if S1 < S2:
    print("S1 < S2")
if S1 >= S2:
    print("S1 >= S2")
